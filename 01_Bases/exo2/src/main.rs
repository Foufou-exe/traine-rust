fn main() {
    // Type booléen : prend la valeur true (vrai) ou false (faux)
    let est_actif: bool = true;

    // Type entier non signé (u32) : un nombre positif stocké sur 32 bits
    let nombre_utilisateurs: u32 = 100;

    // Type caractère (char) : représente une valeur Unicode (avec des guillemets simples)
    let initiale: char = 'R';

    // Affichage formaté des variables
    println!("Actif: {}, Nombre d'utilisateurs: {}, Initiale: {}", est_actif, nombre_utilisateurs, initiale);
}